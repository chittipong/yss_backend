class SiteController extends Controller
{
     public function behaviors()
    {
        return [
            'access' => [
                'class' => AccessControl::className(),
                'ruleConfig'=>[
                  'class'=>AccessRule::className(),  
                ],
                'only' => ['logout', 'signup'],
                'rules' => [
                    [
                        'actions' => ['signup'],
                        'allow' => true,
                        'roles' => ['?'],
                    ],
                    [
                        'actions' => ['logout'],
                        'allow' => true,
                        'roles' => ['@'],
                    ],
                ],
            ],
            'verbs' => [
                'class' => VerbFilter::className(),
                'actions' => [
                    'logout' => ['post'],
                ],
            ],
        ];
    }//end***

    public function actions()
    {
        return [
            'error' => [
                'class' => 'yii\web\ErrorAction',
            ],
            'captcha' => [
                'class' => 'yii\captcha\CaptchaAction',
                'fixedVerifyCode' => YII_ENV_TEST ? 'testme' : null,
            ],
        ];
    }
    
    //USER CHEK PERMISSION********
    public function checkPermission(){
        //USER PROTECTED==========================================
        if(Yii::$app->user->isGuest){
            Yii::$app->session->setFlash('error', 'You must login.');
            return $this->redirect(['site/login']);
        }
         //END USER PROTECTED=====================================
    }//end***

    public function actionIndex()
    {
        //PRODUCT==========================
            $searchModel = new ProductSearch();
            $dataProvider = $searchModel->search(Yii::$app->request->queryParams);
            $dataProvider->pagination->pageSize=3;                              //Set page size
            $dataProvider->sort->defaultOrder=['product_id'=>'DESC'];           //Order by product_id
        
        //NEWS==============================
            $searchNewsModel=new NewsSearch();
            $newsData = $searchNewsModel->search(Yii::$app->request->queryParams);

        //RENDER TO VIEW====================
            return $this->render('index', [
                'searchModel' => $searchModel,
                'dataProvider' => $dataProvider,
                'searchNewsModel'=>$searchNewsModel,
                'newsData'=>$newsData,
            ]);
    }//end***

    public function actionLogin()
    {
        if (!\Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            return $this->goBack();
        }
        return $this->render('login', [
            'model' => $model,
        ]);
    }

    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }

